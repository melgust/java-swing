package gt.edu.miumg.helloworld.modelo;

/**
 *
 * @author melgust
 */
public class Mes {

    int numero;
    int dias;

    public Mes(int numero, int dias) {
        this.numero = numero;
        this.dias = dias;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getDias() {
        return dias;
    }

    public void setDias(int dias) {
        this.dias = dias;
    }

}
